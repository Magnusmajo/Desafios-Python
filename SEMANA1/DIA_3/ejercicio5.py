x = 3
while x > 0:
    print(x)
    x -= 1

# Imprime los números 3, 2 y 1 en líneas separadas.
# Explicación: La variable x se inicializa con el valor 3. El bucle while continúa ejecutándose mientras x sea mayor que 0.
# En cada iteración del bucle, se imprime el valor actual de x y luego se decrementa x en 1 (x -= 1).
# Por lo tanto, el bucle imprimirá 3 en la primera iteración, luego 2 en la segunda, y finalmente 1 en la tercera.
# Cuando x se decrementa a 0, la condición x > 0 ya no se cumple, y el bucle termina.